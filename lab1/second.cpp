// Определяем макрос PR для печати информации об указателе
// Печатает: адрес указателя (x), значение по указателю (*x), адрес переменной указателя (&x)
#define PR(x) printf("x=%u, *x=%d, &x=%u\n",x,*x,&x)

#include <iostream>  // Подключение стандартной библиотеки ввода/вывода

int main()
{
    // Инициализация массива из 3-х целых чисел
    int mas[] = {100, 200, 300};
    
    // Объявление двух указателей на int
    int *ptr1, *ptr2;

    // ptr1 указывает на начало массива (элемент mas[0])
    ptr1 = mas;
    // ptr2 указывает на третий элемент массива (mas[2])
    ptr2 = &mas[2];
    
    // Выводим информацию о ptr1 (адрес, значение, адрес переменной указателя)
    PR(ptr1);
    
    // Увеличиваем ptr1 (он теперь указывает на mas[1])
    ptr1++;
    // Снова выводим информацию о ptr1
    PR(ptr1);
    
    // Выводим информацию о ptr2
    PR(ptr2);
    
    // Увеличиваем ptr2 (теперь он указывает на адрес после mas[2])
    ++ptr2;
    
    // Выводим разницу между указателями (количество элементов между ними)
    printf("ptr2-ptr1=%u\n",ptr2-ptr1);
    
    return 0;  // Завершение программы
}