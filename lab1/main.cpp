#include <iostream>  // Подключаем библиотеку для ввода/вывода

// Объявление функции power (возведение в степень)
int power(int, int);

int main()
{
    int base, exponent, result;  // Переменные для основания, показателя и результата
    
    // Запрашиваем у пользователя основание степени
    std::cout << "Введите основание степени: ";
    std::cin >> base;
    
    // Цикл для вычисления и вывода степеней от 0 до 9
    for(int i = 0; i < 10; i++)
    {
        // Вычисляем base в степени i с помощью функции power
        result = power(base, i);
        // Выводим результат в формате "base^i = result"
        std::cout << base << "^" << i << " = " << result << std::endl;
    }
    
    return 0;  // Завершаем программу
}

// Определение функции power для возведения в степень
int power(int x, int n)
{
    int p = 1;  // Инициализируем результат значением 1 (x^0 = 1)
    
    // Умножаем p на x n раз
    for (int i = 1; i <= n; i++) 
        p *= x;
    
    return p;  // Возвращаем результат x^n
}